class Day:
  stocks = []
  def __init__(self, date, stocks):
    self.date = date
    self.stocks.append(stocks)
  
  def getDate():
    return self.date
  
  def setStocks(stocks):
    self.stocks = stocks
  
  def addStock(stock):
    self.stocks.append(stock)
  
  def getStocks():
    return stocks
  
